pub const RELEASE_MASK: u32 = 1 << 30;

const MOD_SHIFT: u32 = 0x0001;
const MOD_CONTROL: u32 = 0x0004;
const MOD_MOD1: u32 = 0x0008;

const RADIUS: f32 = 8.0;
const H_PAD: f32 = 10.0;
const CAND_H_PAD: f32 = 9.0;
const MIN_CELL_W: f32 = 30.0;
const FONT_SIZE: f32 = 16.0;
const KEY_SIZE: f32 = 11.0;
const COMMENT_SIZE: f32 = 12.0;
const PREEDIT_H: f32 = 26.0;
const CELL_H: f32 = 32.0;
const MAX_COLS: usize = 9;

/// Shared state read by the tray icon and written by the IME thread.
