impl Dispatch<ZwpInputMethodV2, ()> for App {
    fn event(
        state: &mut Self,
        proxy: &ZwpInputMethodV2,
        event: zwp_input_method_v2::Event,
        _: &(),
        _conn: &Connection,
        qh: &QueueHandle<Self>,
    ) {
        match event {
            zwp_input_method_v2::Event::Activate => {
                state.active = true;
                state.keyboard_grab = Some(proxy.grab_keyboard(qh, ()));
                state.create_popup(qh);
                tracing::debug!("IME activated");
            }
            zwp_input_method_v2::Event::Deactivate => {
                state.active = false;
                state.keyboard_grab.take();
                state.engine.reset();
                state.destroy_popup();
                tracing::debug!("IME deactivated");
            }
            zwp_input_method_v2::Event::Done => {
                state.serial = state.serial.wrapping_add(1);
            }
            zwp_input_method_v2::Event::Unavailable => {
                tracing::error!("input method unavailable — another IME may be running");
            }
            _ => {}
        }
    }
}

impl Dispatch<ZwpInputMethodKeyboardGrabV2, ()> for App {
    fn event(
        state: &mut Self,
        _proxy: &ZwpInputMethodKeyboardGrabV2,
        event: zwp_input_method_keyboard_grab_v2::Event,
        _: &(),
        _conn: &Connection,
        qh: &QueueHandle<Self>,
    ) {
        match event {
            zwp_input_method_keyboard_grab_v2::Event::Keymap { format, fd, size } => {
                if format != WEnum::Value(wl_keyboard::KeymapFormat::XkbV1) {
                    return;
                }
                // Forward keymap to virtual keyboard so it can process key events correctly.
                if let Some(vk) = &state.vk {
                    // dup fd so both VK and xkb can use it independently
                    let dup_fd = unsafe { libc::dup(fd.as_raw_fd()) };
                    if dup_fd >= 0 {
                        let owned = unsafe { std::os::unix::io::OwnedFd::from_raw_fd(dup_fd) };
                        vk.keymap(wl_keyboard::KeymapFormat::XkbV1 as u32, owned.as_fd(), size);
                    }
                }
                let Ok(mmap) = (unsafe {
                    memmap2::MmapOptions::new()
                        .len(size as usize)
                        .map_raw_read_only(&fd)
                }) else {
                    return;
                };
                let s = unsafe {
                    let bytes = std::slice::from_raw_parts(mmap.as_ptr(), size as usize - 1);
                    std::str::from_utf8_unchecked(bytes).to_string()
                };
                if let Some(km) = xkb::Keymap::new_from_string(
                    &state.xkb_context,
                    s,
                    xkb::KEYMAP_FORMAT_TEXT_V1,
                    xkb::KEYMAP_COMPILE_NO_FLAGS,
                ) {
                    state.xkb_state = Some(xkb::State::new(&km));
                    state.xkb_keymap = Some(km);
                }
            }
            zwp_input_method_keyboard_grab_v2::Event::Key {
                key,
                state: ks,
                time,
                ..
            } => match ks {
                WEnum::Value(wl_keyboard::KeyState::Pressed) => {
                    state.handle_key_press(key, time, qh);
                }
                WEnum::Value(wl_keyboard::KeyState::Released) => {
                    state.handle_key_release(key, time, qh);
                }
                _ => {}
            },
            zwp_input_method_keyboard_grab_v2::Event::Modifiers {
                mods_depressed,
                mods_latched,
                mods_locked,
                group,
                ..
            } => {
                // Update virtual keyboard modifier state so Ctrl+C etc. are delivered correctly.
                if let Some(vk) = &state.vk {
                    vk.modifiers(mods_depressed, mods_latched, mods_locked, group);
                }
                if let Some(xs) = &mut state.xkb_state {
                    xs.update_mask(mods_depressed, mods_latched, mods_locked, 0, 0, group);
                    let mut m = 0u32;
                    if xs.mod_name_is_active(xkb::MOD_NAME_SHIFT, xkb::STATE_MODS_EFFECTIVE) {
                        m |= MOD_SHIFT;
                    }
                    if xs.mod_name_is_active(xkb::MOD_NAME_CTRL, xkb::STATE_MODS_EFFECTIVE) {
                        m |= MOD_CONTROL;
                    }
                    if xs.mod_name_is_active(xkb::MOD_NAME_ALT, xkb::STATE_MODS_EFFECTIVE) {
                        m |= MOD_MOD1;
                    }
                    state.mods = m;
                }
            }
            _ => {}
        }
    }
}

